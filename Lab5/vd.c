#include <stdio.h>

int main(){
	//while
	printf("Vong lap while:\n");
	int i = 1;
	while(i <=10){
		printf("%d ", i);
		i++;
	}
    //for
    printf("\nVong lap for:\n");
    i = 1;
    for(; i<=10; i++){
    	printf("%d ", i);
	}
	//do while
	printf("\nVong lap do-while:\n");
	i = 1;
	do{
		printf("%d ", i);
		i++;
	}while(i<=10);
	
	
	
	
    
    return 0;
}

